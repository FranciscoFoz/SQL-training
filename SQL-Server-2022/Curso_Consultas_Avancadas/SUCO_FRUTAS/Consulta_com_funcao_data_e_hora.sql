SELECT 
NOME,
DATA_DE_NASCIMENTO,
CONCAT(
	'Dia', DATENAME(DAY,DATA_DE_NASCIMENTO),
	' (',LOWER(DATENAME(WEEKDAY,DATA_DE_NASCIMENTO)),')',	
	' de ', LOWER(DATENAME(MONTH,DATA_DE_NASCIMENTO)),	
	' de ', DATENAME(YEAR,DATA_DE_NASCIMENTO)
) AS NOME_POR_EXTENSO
FROM TABELA_DE_CLIENTES;

