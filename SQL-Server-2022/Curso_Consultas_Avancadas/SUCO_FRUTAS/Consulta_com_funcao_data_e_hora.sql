SELECT 
GETDATE() AS DATA_ATUAL,
DATEADD(DAY,30,GETDATE()) AS PRAXO_30_DIAS,
DATEDIFF(DAY,'2023-01-01',GETDATE()) AS DIAS_DESDE_O_INICIO_DO_ANO
;


